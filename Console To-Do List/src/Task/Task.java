package Task;

public class Task {

    private long id;
    private String message;
    private String date;

    public void setId(long id) {
        this.id = id;
    }

    public long getId() {
        return id;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }


}
